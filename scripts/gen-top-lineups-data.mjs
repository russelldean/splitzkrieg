import sql from 'mssql';
import { readFileSync, writeFileSync } from 'fs';

const envContent = readFileSync('.env.local', 'utf8');
for (const line of envContent.split('\n')) {
  const match = line.match(/^([^#=]+)=(.*)$/);
  if (match) process.env[match[1].trim()] = match[2].trim();
}

const dbConfig = {
  server: process.env.AZURE_SQL_SERVER,
  database: process.env.AZURE_SQL_DATABASE,
  user: process.env.AZURE_SQL_USER,
  password: process.env.AZURE_SQL_PASSWORD,
  options: { encrypt: true, trustServerCertificate: false },
};

const TEAMS = [
  'Alley Oops', 'Bowl Durham', 'E-Bowla', 'Fancy Pants', "Grandma's Teeth",
  'Gutterglory', 'Guttermouths', 'Guttersnipes', 'HOT FUN', 'Hot Shotz',
  'Living on a Spare', 'Lucky Strikes', 'Pin-Ups', 'Smoke-a-Bowl',
  'Sparadigm Shift', 'Stinky Cheese', 'The Boom Kings', 'Thoughts and Spares',
  'Valley of the Balls', 'Wild Llamas',
];

async function main() {
  const pool = await new sql.ConnectionPool(dbConfig).connect();
  const results = {};

  for (const teamName of TEAMS) {
    const req = pool.request();
    req.input('teamName', sql.NVarChar, teamName);
    const res = await req.query(`
      SELECT s.seasonID, s.week, s.bowlerID, b.bowlerName, CAST(b.isActive AS INT) as isActive
      FROM scores s
      JOIN bowlers b ON s.bowlerID = b.bowlerID
      JOIN teams t ON s.teamID = t.teamID
      WHERE t.teamName = @teamName AND s.bowlerID != 629
      ORDER BY s.seasonID, s.week, s.bowlerID
    `);

    // Build per-night map
    const nightMap = new Map();
    const bowlerMeta = new Map();
    for (const row of res.recordset) {
      bowlerMeta.set(row.bowlerID, { name: row.bowlerName, active: row.isActive === 1 });
      const key = `${row.seasonID}-${row.week}`;
      if (!nightMap.has(key)) nightMap.set(key, { seasonID: row.seasonID, ids: [] });
      nightMap.get(key).ids.push(row.bowlerID);
    }

    const totalNights = nightMap.size;

    // Group by lineup key
    const lineupMap = new Map();
    for (const { seasonID, ids } of nightMap.values()) {
      const key = ids.sort((a, b) => a - b).join(',');
      if (!lineupMap.has(key)) lineupMap.set(key, { count: 0, seasons: new Set() });
      lineupMap.get(key).count++;
      lineupMap.get(key).seasons.add(seasonID);
    }

    const uniqueLineups = lineupMap.size;

    const top3 = [...lineupMap.entries()]
      .sort((a, b) => b[1].count - a[1].count)
      .slice(0, 3)
      .map(([key, data]) => {
        const ids = key.split(',').map(Number);
        return {
          ids,
          names: ids.map(id => bowlerMeta.get(id)?.name ?? `Bowler ${id}`),
          active: ids.map(id => bowlerMeta.get(id)?.active ?? false),
          count: data.count,
          seasons: [...data.seasons].sort((a, b) => a - b),
        };
      });

    results[teamName] = { name: teamName, totalNights, uniqueLineups, lineups: top3 };
    process.stderr.write(`${teamName}: ${totalNights} nights, ${uniqueLineups} unique lineups, top: ${top3[0]?.count ?? 0}\n`);
  }

  await pool.close();

  // Generate TypeScript
  let out = `// AUTO-GENERATED by scripts/gen-top-lineups-data.mjs\n`;
  out += `// Do not edit manually — re-run the script to regenerate\n\n`;
  out += `export type TopLineup = { ids: number[]; names: string[]; active: boolean[]; count: number; seasons: number[] };\n`;
  out += `export type TopTeamData = { name: string; totalNights: number; uniqueLineups: number; lineups: TopLineup[] };\n\n`;
  out += `export const ALL_TOP_LINEUPS: TopTeamData[] = [\n`;

  for (const teamName of TEAMS) {
    const { name, totalNights, uniqueLineups, lineups } = results[teamName];
    out += `  {\n`;
    out += `    name: ${JSON.stringify(name)},\n`;
    out += `    totalNights: ${totalNights},\n`;
    out += `    uniqueLineups: ${uniqueLineups},\n`;
    out += `    lineups: [\n`;
    for (const l of lineups) {
      out += `      { ids: [${l.ids.join(',')}], names: [${l.names.map(n => JSON.stringify(n)).join(',')}], active: [${l.active.join(',')}], count: ${l.count}, seasons: [${l.seasons.join(',')}] },\n`;
    }
    out += `    ],\n`;
    out += `  },\n`;
  }

  out += `];\n`;

  writeFileSync('src/app/lucky-strikes/lineups/top/top-lineups-data.ts', out);
  console.log(`Written ${out.split('\n').length} lines to src/app/lucky-strikes/lineups/top/top-lineups-data.ts`);
}

main().catch(err => { console.error(err); process.exit(1); });
